    The Shaheed Minar (English: Martyrs' Monument), formerly known as the
    Ochterlony Monument, is a monument in Kolkata that was erected in 1828 in
    memory of Major-general Sir David Ochterlony, commander of the British
    East India Company, to commemorate both his successful defense of Delhi
    against the Marathas in 1804 and the victory of the East India Companyâ€™s
    armed forces over the Gurkhas in the Anglo-Nepalese War, also known as the
    Gurkha War. The monument was constructed in his memory. It was designed by
    J. P. Parker and paid for from public funds.[2]
    On 9 August 1969, it was rededicated to the memory of the martyrs of the
    Indian freedom movement and renamed the "Shaheed Minar," which means
    "martyrs' monument" in both Bengali and Hindi, by the then United Front
    Government in memory of the martyrs of the Indian independence
    movement[clarification needed]. The present[when?] government has decided
    to illuminate the tower during evenings and allow visitors to the top. The
    last[when?] people to have been up there were former governor Gopal
    Krishna Gandhi and his family.
