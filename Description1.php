The Indian Museum in Central Kolkata, West Bengal, India, also
referred to as the Imperial Museum at Calcutta in colonial-era
texts,[2][3] is the ninth oldest museum in the world,[citation needed] the
oldest and largest museum in India. It has rare
collections of antiques, armour and ornaments, fossils, skeletons, mummies
and Mughal paintings. It was founded by the Asiatic Society of Bengal in
Kolkata (Calcutta), India, in 1814. The founder curator was Nathaniel
Wallich, a Danish botanist.
It has six sections comprising thirty five galleries of cultural and
scientific artifacts namely Indian art, archaeology, anthropology,
geology, zoology and economic botany. Many rare and unique specimens, both
Indian and trans-Indian, relating to humanities and natural sciences, are
preserved and displayed in the galleries of these sections. In particular
the art and archaeology sections hold collections of international
importance.
It is an autonomous organization under Ministry of Culture, Government
of India. The present Director of the Indian Museum is Shri Arijit Dutta
Choudhury who is also the Director General, NCSM and having the additional
charge of Director General of National Library.[11]
Administrative control of the Cultural sections, viz. Art, Archaeology
and Anthropology rests with the Board of Trustees under its Directorate,
and that of the three other science sections is with the geological survey
of India, the zoological survey of India and the Botanical survey of
India. The museum Directorate has eight co-ordinating service units:
Education, preservation, publication, presentation, photography, medical,
modelling and library. This multipurpose institution with
multidisciplinary activities is being included as an Institute of national
importance in the seventh schedule of the Constitution of India.[12]

