The Writers' Buildings, often shortened to just Writers', is the
official secretariat building of the state government of West Bengal in
Kolkata, India. The 150-meter long building covers the entire northern
stretch of the iconic Lal Dighi pond at the centre of historic B.B.D.
Bagh, long considered as the administrative and business hub of the city.
It originally served as the principal administrative office for writers
(junior clerks) of the British East India Company (EIC). Designed by
Thomas Lyon in 1777, the Writers' Building has gone through a long series
of extensions over the centuries. Since India's independence in 1947, it
housed the office of the Chief Minister of West Bengal, cabinet ministers
and other senior officials, until 4 October 2013, when a major restoration
of the building was announced. The majority of government departments were
subsequently moved out to a new repurposed building named Nabanna in
Howrah on a temporary basis.
The building has been called a mini-township of sorts with a built-up area
of around 550,000 square feet. Before the shifting of the state
secretariat, the building housed 34 departments of the state government,
and served as the office for approximately 6,000 employees.[1] As of
October 2020, the renovation of the building continues.
