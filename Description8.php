    The Victoria Memorial is a large marble building in Central Kolkata,
    which was built between 1906 and 1921. It is dedicated to the memory of
    Queen Victoria, Empress of India from 1876 to 1901. It is now a museum
    under the auspices of the Ministry of Culture, and is the largest monument
    in the world which is dedicated to a royal.[2] The memorial lies on the
    Maidan and is one of the famous monuments of Kolkata.

    The Victoria Memorial is an iconic and grand monument that holds historical and cultural significance. Here's some information about the Victoria Memorial:

History: The Victoria Memorial was built to commemorate Queen Victoria's 25-year reign over India (1837-1901). It was designed by British architect William Emerson and construction began in 1906. The memorial was completed in 1921, and it was officially inaugurated by the Prince of Wales (who later became King Edward VIII) in the same year.

Architecture: The Victoria Memorial is a splendid example of Indo-Saracenic architecture, a style that blends elements of British and Mughal architecture with other Indian architectural styles. The memorial is constructed using white Makrana marble, and its design is inspired by the Taj Mahal in Agra. It stands in a sprawling garden, providing a serene environment for visitors.

Collection: The Victoria Memorial houses a vast collection of artifacts, memorabilia, and historical exhibits from the British colonial period in India. Inside the memorial, visitors can find various paintings, sculptures, rare photographs, historical documents, and other items that offer insights into India's colonial past.

Galleries: The memorial features several galleries, including the Royal Gallery, the Portrait Gallery, the Calcutta Gallery, and the Central Hall, which houses a massive statue of Queen Victoria. The galleries showcase different aspects of India's history and the life and times of Queen Victoria.

Tourist Attraction: The Victoria Memorial is one of the most popular tourist attractions in Kolkata and attracts both local and international visitors. The beautiful architecture and the historical significance make it a must-visit destination for anyone exploring the city's heritage.

