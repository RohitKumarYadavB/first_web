Marble Palace is a palatial nineteenth-century mansion in North
Kolkata. It is located at 46, Muktaram Babu Street, Kolkata 700007. It is
one of the best-preserved and most elegant houses of nineteenth-century
Calcutta.[1] The mansion is famous for its marble walls, floors, and
sculptures, from which it derives its name.

