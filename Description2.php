Belur Math is a significant religious and spiritual center located in Belur, a suburb of Kolkata (Calcutta), West Bengal, India. It is renowned for being the headquarters of the Ramakrishna Math and Ramakrishna Mission, two organizations that propagate the teachings and ideals of Sri Ramakrishna Paramahamsa and Swami Vivekananda, two prominent spiritual leaders of India.

The main temple at Belur Math is dedicated to Sri Ramakrishna Paramahamsa, who was a 19th-century saint and mystic known for his spiritual experiences and teachings that emphasized the harmony of all religions. Swami Vivekananda, his foremost disciple, played a pivotal role in spreading his guru's teachings both in India and internationally. He is also known for his famous speech at the Parliament of the World's Religions in Chicago in 1893, where he addressed the audience as "Sisters and Brothers of America."

Belur Math was established in 1897 by Swami Vivekananda himself as the headquarters for the Ramakrishna Math and Ramakrishna Mission. The architecture of the temple complex is a blend of Hindu, Islamic, and Christian elements, symbolizing the unity of all religions. The serene and picturesque campus attracts thousands of visitors and devotees every year who come to seek spiritual solace, learn about the teachings of Sri Ramakrishna and Swami Vivekananda, and participate in various philanthropic and educational activities conducted by the mission.

The Ramakrishna Math and Ramakrishna Mission are involved in several social welfare and humanitarian activities, such as providing educational assistance, healthcare services, disaster relief, and rural development initiatives.

Belur Math remains an essential landmark in the spiritual landscape of India and continues to inspire people from all walks of life to lead a life of service, devotion, and self-realization.



