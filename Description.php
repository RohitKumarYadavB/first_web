Howrah Bridge, also known as Rabindra Setu, is an iconic cantilever bridge located in Kolkata (formerly known as Calcutta), West Bengal, India. It is one of the most famous landmarks in the city and a significant engineering marvel.

The Howrah Bridge is a balanced cantilever bridge over the Hooghly River
in West Bengal, India. Commissioned in 1943,[9][11] the bridge was
originally named the New Howrah Bridge, because it replaced a pontoon
bridge at the same location linking the cities of Howrah and Kolkata
(Calcutta). On 14 June 1965, it was renamed Rabindra Setu after the great
Bengali poet Rabindranath Tagore, who was the first Indian and Asian Nobel
laureate.[11] It is still popularly known as the Howrah Bridge.

The bridge is one of four on the Hooghly River and is a famous symbol of
Kolkata and West Bengal. The other bridges are the Vidyasagar Setu
(popularly called the Second Hooghly Bridge), the Vivekananda Setu and the
relatively new Nivedita Setu. It carries a daily traffic of approximately
100,000 vehicles[12] and possibly more than 150,000 pedestrians,[10]
easily making it the busiest cantilever bridge in the world.[13] The
third-longest cantilever bridge at the time of its construction,[14] the
Howrah Bridge is currently the sixth-longest bridge of its type in the
world.[15]

Key features of the Howrah Bridge:

Structure: The bridge spans over the Hooghly River, connecting the city of Howrah to Kolkata. It has a total length of approximately 705 meters (2,313 feet) and a central span of 457.5 meters (1,499 feet). At the time of its construction, it was one of the longest bridges of its type in the world.

Design: Howrah Bridge is a cantilever bridge, which means it is supported on vertical pillars, and there are no suspended cables or towers. The design allows it to bear the load of vehicular and pedestrian traffic without the need for any intermediate support in the river.

Historical Significance: The construction of Howrah Bridge was completed in 1942 during British rule in India. It played a crucial role in improving connectivity between the two cities and became a symbol of Kolkata's growth and development during that period.

All-Weather Transport: Howrah Bridge is vital for the transportation of goods and people, as it serves as a major route for road traffic, including cars, buses, and pedestrians. Thousands of vehicles and pedestrians cross the bridge every day, making it one of the busiest in the world.

Architectural Highlights: The bridge's artistic illumination at night adds to its beauty and attracts many tourists and locals alike. The structure has also been featured in numerous films, making it an integral part of Kolkata's cultural identity.

Name Change: In 1965, the bridge was officially renamed Rabindra Setu in honor of the Nobel laureate Rabindranath Tagore, who was a prominent figure in Bengali literature and culture.

