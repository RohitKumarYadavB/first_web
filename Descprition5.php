    Raj Bhavan is the official residence of the governor of West Bengal,
    located in Kolkata, the capital of the Indian state of West Bengal. Built
    in 1803, it was known as Government House before Indian independence.
    After the transfer of power from the East India Company to the British
    Crown in 1858, it became the official residence of the Viceroy of India,
    moving here from the Belvedere Estate. With the shifting of the British
    Indian capital from then Calcutta to Delhi in 1911, it became the official
    residence of Lieutenant Governor of Bengal. Since independence in 1947 it
    serves as the official residence of the Governor of West Bengal and came
    to be known as the Raj Bhavan, a name it shares with the official
    residences of other states' governors

    

