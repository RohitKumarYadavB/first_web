    he building of the Town Hall was made in Roman-Doric style in 1813. At
    first, the hall was placed under a committee, which allowed the public to
    use the hall under such terms and conditions as were fixed by the
    Government. The public could visit the ground floor hall to see statues
    and large size portrait paintings but they were not allowed indiscriminate
    access to the upper storey. Applications for the use of the upper storey
    were to be made to the committee.
    In 1867 Town Hall came under the management of the municipal authority,
    the Justices of Peace for the improvement of the town of Kolkata (later on
    the Calcutta Corporation). In the 1870s, at the time of the Chief Justice
    Richard Couch, when the present building of the High Court was being
    built, the Town Hall was temporarily used for judicial purposes. In 1871,
    one of the Puisne Judges, Sir John Paxton Norman was assassinated by a
    fanatic Muslim of the Wahabi sect, while coming down the steps of the Town
    Hall. In the year of 1897 the Town Hall had been renovated at a cost of
    about Rs. 1.126 million.
