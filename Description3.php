Fort William is a fort in Hastings, Calcutta (Kolkata). It was built
during the early years of the Bengal Presidency of British India. It sits
on the eastern banks of the Hooghly River, the major distributary of the
River Ganges. One of Kolkata's most enduring Raj-era edifices, it extends
over an area of 70.9 hectares.
The fort was named after King William III.[1] In front of the Fort is the
Maidan, the largest park in the country. An internal guard room became the
Black Hole of Calcutta. Today it is the Headquarters of Eastern Command of
the Indian Army.

Fort William is a prominent historical landmark located in Kolkata (formerly Calcutta), the capital city of the Indian state of West Bengal. It holds great significance in India's colonial history and serves as a testament to the country's past under British rule.

Here's some information about Fort William in Kolkata:

History: Fort William was built in the late 18th century during the British Raj in India. The construction began in 1758 by Robert Clive, the British military officer, and was completed in 1781. It was named after King William III of England.

Purpose: The primary purpose of Fort William was to serve as a fortified military stronghold for the British East India Company. It was strategically situated on the eastern bank of the Hooghly River, providing a commanding position to protect the British trading interests in the region and to counter potential invasions.

Architecture: The fort's architecture reflects a blend of European and Indian styles. It covers a vast area and is surrounded by high defensive walls, bastions, and a wide moat. The structure was designed to withstand attacks and was equipped with military facilities, including barracks, a hospital, and warehouses.

Black Hole of Calcutta: One infamous incident associated with Fort William is the "Black Hole of Calcutta." In 1756, after Siraj-ud-Daulah, the Nawab of Bengal, captured the fort, a large number of British prisoners were allegedly confined in a small, overcrowded dungeon called the "Black Hole." Many prisoners died due to suffocation and heat, which became a tragic event and fueled British sentiments against the Nawab.

Present Day: Today, Fort William continues to serve as an operational military base for the Indian Army. The area within the fort is restricted to the public, but visitors can catch glimpses of its imposing exterior from various viewpoints across Kolkata, especially from the popular tourist spot, the Victoria Memorial.

Fort William remains a significant historical landmark, reminding people of India's colonial past and the complex history of the city of Kolkata.





